public class StackViewData {
    private String sumTotalText;
    private long time;
    private String bottomText;
    private String oneLevelText;
    private String twoLevelText;
    private int sumTotal;
    private int oneLevelTotal;
    private int twoLevelTotal;


    public String getSumTotalText() {
        return sumTotalText;
    }

    public void setSumTotalText(String sumTotalText) {
        this.sumTotalText = sumTotalText;
    }

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }

    public String getBottomText() {
        return bottomText;
    }

    public void setBottomText(String bottomText) {
        this.bottomText = bottomText;
    }

    public String getOneLevelText() {
        return oneLevelText;
    }

    public void setOneLevelText(String oneLevelText) {
        this.oneLevelText = oneLevelText;
    }

    public String getTwoLevelText() {
        return twoLevelText;
    }

    public void setTwoLevelText(String twoLevelText) {
        this.twoLevelText = twoLevelText;
    }

    public int getSumTotal() {
        return sumTotal;
    }

    public void setSumTotal(int sumTotal) {
        this.sumTotal = sumTotal;
    }

    public int getOneLevelTotal() {
        return oneLevelTotal;
    }

    public void setOneLevelTotal(int oneLevelTotal) {
        this.oneLevelTotal = oneLevelTotal;
    }

    public int getTwoLevelTotal() {
        return twoLevelTotal;
    }

    public void setTwoLevelTotal(int twoLevelTotal) {
        this.twoLevelTotal = twoLevelTotal;
    }
}
